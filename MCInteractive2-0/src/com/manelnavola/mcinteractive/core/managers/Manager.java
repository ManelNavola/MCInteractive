package com.manelnavola.mcinteractive.core.managers;

/**
 * Abstract class to represent a Manager
 * @author Manel Navola
 *
 */
public abstract class Manager {
	
	/**
	 * Initializes the manager
	 */
	public abstract void start();
	
	/**
	 * Stops the manager
	 */
	public abstract void stop();
	
}
